# latihan tipe data

# boolean
a = True
b = False
print(a, b)

# integer
c = 5
d = 19
print(c, d)

# float
e = 10.3
f = 76.317
print(e, f)

# string
g = "belajar python"
h = 'belajar python'
print(g, h)

# mengecek
print(f"{type(a)} {type(b)}\n"
    f"{type(c)} {type(d)}\n"
    f"{type(e)} {type(f)}\n"
    f"{type(g)} {type(h)}")